inline LevelGrid InitializeLevelGridOfDimensions(const unsigned int cols, const unsigned int rows)
{
    LevelGrid result(rows);

    for (auto& row : result)
    {
        row.resize(cols);
    }

    return result;
}

inline glm::vec3 LevelGridCoordsToPosition(const int col, const int row)
{
    return glm::vec3(col * OVERWORLD_TILE_SIZE, 0.0f, row * OVERWORLD_TILE_SIZE);
}

