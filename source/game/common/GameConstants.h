struct TileCoords
{
    TileCoords()
        : mCol(0)
        , mRow(0)
    {
    }

    TileCoords(const int col, const int row)
        : mCol(col)
        , mRow(row)
    {
    }

    int mCol;
    int mRow;
};

inline bool operator == (const TileCoords& lhs, const TileCoords& rhs)
{
    return lhs.mCol == rhs.mCol && lhs.mRow == rhs.mRow;
}

